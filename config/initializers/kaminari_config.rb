# frozen_string_literal: true

# Kaminariのページネーション設定を行うファイル
# Kaminariは、Railsアプリケーションでページネーション機能を提供するgemです

Kaminari.configure do |config|
  # 1ページあたりの表示件数を10件に設定
  # 例: 投稿一覧で1ページに表示される投稿数が10件になります
  config.default_per_page = 10

  # ウィンドウサイズを2に設定（現在のページの前後2ページまで表示）
  # 例: 現在が5ページ目の場合、3,4,5,6,7ページのリンクが表示されます
  config.window = 2

  # 外側のウィンドウサイズを1に設定
  # ページネーションの最初と最後の部分に表示するページ数
  # 例: 1ページ目と最終ページの近くで1ページ分表示されます
  config.outer_window = 1

  # 左側に表示するページ数を1に設定
  # ページネーションの左端に常に表示されるページ数
  # 例: 常に1ページ目のリンクが表示されます
  config.left = 1

  # 右側に表示するページ数を1に設定
  # ページネーションの右端に常に表示されるページ数
  # 例: 常に最終ページのリンクが表示されます
  config.right = 1

  # 1ページあたりの最大表示件数の制限（未設定）
  # デフォルトでは制限なし
  # config.max_per_page = nil

  # 最大ページ数の制限（未設定）
  # デフォルトでは制限なし
  # config.max_pages = nil

  # 最初のページでのパラメータ挙動設定（未設定）
  # falseの場合、1ページ目ではページパラメータを付けない
  # config.params_on_first_page = false
end
